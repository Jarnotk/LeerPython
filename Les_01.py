# Python is een programmeertaal. Dat houd in: 
# - Je schrijft tekst 
# - Python leest de geschreven tekst 
# - De computer voert het uit
# 
# Python is op dit moment een snel groeiende programmeertaal. Dit komt vooral omdat Python makkelijk
# te schrijven/lezen is, veel leerbronnen heeft en een grote community heeft.
# 
# In deze les leren we de basis van Python.
# 
# We beginnen met het schrijven van een eenvoudige tekst.
# 
# Onderstaande code zorgt ervoor dat de tekst "Hallo, ik leer Python" wordt weergegeven op je scherm.
# Dat komt door de "print()" functie.

print("Hallo, ik leer Python")
	# Tekst tussen "" betekend dat het gaat om een "string".

print(10)
	# Getallen tussen () zonder "" betekend dat het gaat om een "integer", een getal. Hiermee kan gerekend worden.

print(10 + 5)
	# Dit is een som. De "print()" functie zal alleen het antwoord weergeven en niet de som zelf.
	# Als je de som wilt weergegen doe je:

print("10 + 5 = ", 10 + 5)
	# "10 + 5 = " is een string en 10 + 5 is een integer. De , zorgt ervoor dat de
	# string en het antwoord worden weergegeven als 1 stuk tekst.